#!/usr/bin/env node
// Renders suite-design/overnight/PROGRESS.md from progress.json.
// Plain Node (>=20), no dependencies. Run from anywhere:
//   node suite-design/overnight/render-progress.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const inputPath = join(here, "progress.json");
const outputPath = join(here, "PROGRESS.md");

const DIFFICULTY_WEIGHTS = { S: 1, M: 2, L: 3, XL: 5 };
const UNPLANNED_WEIGHT = DIFFICULTY_WEIGHTS.M; // unplanned difficulty counts as M
const BAR_WIDTH = 10;

function bar(pct, width = BAR_WIDTH) {
  const clamped = Math.max(0, Math.min(100, pct));
  const filled = Math.round((clamped / 100) * width);
  return "█".repeat(filled) + "░".repeat(width - filled);
}

function difficultyBadge(difficulty) {
  return difficulty ? `[${difficulty}]` : "[?]";
}

function remainingLabel(hours) {
  if (hours === 0) return "done";
  if (hours == null) return "~?h";
  return `~${hours}h left`;
}

function groupOf(pkg) {
  if (pkg.status === "parked") return "parked";
  if (pkg.stage_progress >= 100) return "done";
  if (pkg.status === "pending") return "pending";
  return "in flight";
}

function renderPackage(pkg) {
  const badge = difficultyBadge(pkg.difficulty);
  const line =
    `- **#${pkg.id} ${pkg.title}** ${badge} — ` +
    `\`${bar(pkg.stage_progress)}\` ${pkg.stage_progress}% ` +
    `(${pkg.status}, ${remainingLabel(pkg.remaining_guess_h)})`;
  return pkg.note ? `${line}\n  - ${pkg.note}` : line;
}

const data = JSON.parse(readFileSync(inputPath, "utf8"));
const packages = [...data.packages].sort((a, b) => a.id - b.id);

const groups = { done: [], "in flight": [], pending: [], parked: [] };
for (const pkg of packages) groups[groupOf(pkg)].push(pkg);

// Overall difficulty-weighted mission progress.
let weightedDone = 0;
let weightTotal = 0;
for (const pkg of packages) {
  const weight = DIFFICULTY_WEIGHTS[pkg.difficulty] ?? UNPLANNED_WEIGHT;
  weightTotal += weight;
  weightedDone += weight * Math.max(0, Math.min(100, pkg.stage_progress));
}
const overallPct = weightTotal ? Math.round(weightedDone / weightTotal) : 0;

const remainingTotal = packages.reduce(
  (sum, pkg) => sum + (pkg.remaining_guess_h ?? 0),
  0,
);

const lastUpdated = packages.reduce(
  (latest, pkg) => (pkg.updated_at > latest ? pkg.updated_at : latest),
  "",
);

const sections = [
  ["done", "Done"],
  ["in flight", "In flight"],
  ["pending", "Pending"],
  ["parked", "Parked"],
];

const lines = [
  "# Mission Progress",
  "",
  "> Generated by `render-progress.mjs` from `progress.json`. Do not edit by hand.",
  "",
  `**Mission** \`${bar(overallPct, 20)}\` ${overallPct}% (difficulty-weighted; S=1 M=2 L=3 XL=5, unplanned=M) — ~${remainingTotal}h guessed remaining`,
  "",
];

for (const [key, heading] of sections) {
  const members = groups[key];
  if (!members.length) continue;
  lines.push(`## ${heading} (${members.length})`, "");
  for (const pkg of members) lines.push(renderPackage(pkg));
  lines.push("");
}

lines.push(`---`, `Last updated: ${lastUpdated}`, "");

writeFileSync(outputPath, lines.join("\n"), "utf8");
console.log(`Wrote ${outputPath} (mission ${overallPct}%)`);
